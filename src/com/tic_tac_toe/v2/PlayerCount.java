package com.tic_tac_toe.v2;

public class PlayerCount {
    private int player1Count;
    private int player2Count;

    public int getPlayer1Count() {
        return player1Count;
    }
    public void incrementPlayer1Count() {
        player1Count++;
    }
    public int getPlayer2Count() {
        return player2Count;
    }
    public void incrementPlayer2Count() {
        player2Count++;
    }
}
